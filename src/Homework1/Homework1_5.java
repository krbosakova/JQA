package Homework1;

public class Homework1_5 {
    public static void main(String[] args) {
        //Да се изведат числата от 1 до 100 във
        // възходящ и в низходящ ред и да се отпечатат получените резултати с помоща на for цикъл

        for (int i = 1; i <= 100; i++) {
            System.out.println(i);
        }
        for (int i = 100; i >= 1; i--) {
            System.out.println(i);
        }
    }
}
